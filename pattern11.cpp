class Solution {
  public:
    void printTriangle(int n) {
        // code here
        for(int i=1;i<=n;i++){
            int bit = i%2 ? 1 : 0;
            
            for(int j = 0;j<i;j++){
                cout<<bit<<" ";
                bit =bit==1? 0 : 1;
            }
            cout<<"\n";
        }
        
    }
};

